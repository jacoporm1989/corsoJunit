package it.valdicogne;

public class Calcolatrice {

    public int divisioneInteri(int dividendo,int divisore){
        return dividendo/divisore;
    }

    public int sottrazione(int n1,int n2){
        return n1 - n2;
    }

    public int addizione(int n1,int n2){
        return n1 + n2;
    }

}
